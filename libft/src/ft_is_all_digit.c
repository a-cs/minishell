#include "../includes/libft.h"

int	ft_is_all_digit(char *c, int len)
{
	int	i;

	i = 0;
	while (i < len)
	{
		if (!(c[i] >= '0' && c[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
